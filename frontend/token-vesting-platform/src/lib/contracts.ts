// Helper function to validate and cast addresses
export function getContractAddress(envVar: string | undefined, fallback: string): `0x${string}` {
  const address = envVar || fallback
  if (!address.startsWith("0x")) {
    throw new Error(`Invalid contract address: ${address}`)
  }
  return address as `0x${string}`
}

